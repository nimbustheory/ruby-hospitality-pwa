// Helper function to get featured content
export function getFeaturedVenues(): Venue[] {
  return venues.filter(v => v.isFeatured);
}

export function getFeaturedEvents(): Event[] {
  return events.filter(e => e.isFeatured && e.status === 'upcoming');
}

export function getFeaturedOffers(): Offer[] {
  return offers.filter(o => o.isFeatured);
}

export function getVenueById(id: string): Venue | undefined {
  return venues.find(v => v.id === id || v.slug === id);
}

export function getEventById(id: string): Event | undefined {
  return events.find(e => e.id === id);
}

export function getOfferById(id: string): Offer | undefined {
  return offers.find(o => o.id === id);
}

export function getEventsByVenue(venueId: string): Event[] {
  return events.filter(e => e.venueId === venueId && e.status === 'upcoming');
}

export function getUpcomingEvents(): Event[] {
  return events
    .filter(e => e.status === 'upcoming')
    .sort((a, b) => new Date(a.startDateTime).getTime() - new Date(b.startDateTime).getTime());
}